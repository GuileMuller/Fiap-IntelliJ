public class Endereco {


    private int codigo;
    private String nome;
    private int numero;
    private String estado;
    private String tipo;
    private String cidade;

    public Endereco(int codigo, String nome, int numero, String estado, String tipo, String cidade) {
        this.codigo = codigo;
        this.nome = nome;
        this.numero = numero;
        this.estado = estado;
        this.tipo = tipo;
        this.cidade = cidade;

    }
}
